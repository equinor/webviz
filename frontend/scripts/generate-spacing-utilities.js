#!/usr/bin/env node
/**
 * Generates src/styles/theme/spacing.css from spacing-tokens.config.js.
 *
 * Usage:
 *   npm run generate:spacing-utilities
 */

import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import { tokens, densityOverrides } from "../src/styles/theme/spacing-tokens.config.js";

const __dirname = dirname(fileURLToPath(import.meta.url));

const OUTPUTS = {
    tokens: resolve(__dirname, "../src/styles/theme/tokens.css"),
    spacing: resolve(__dirname, "../src/styles/theme/spacing.css"),
    sizing: resolve(__dirname, "../src/styles/theme/sizing.css"),
    position: resolve(__dirname, "../src/styles/theme/position.css"),
};

const PX_PROPERTIES = {
    p: "padding",
    pt: "padding-top",
    pr: "padding-right",
    pb: "padding-bottom",
    pl: "padding-left",
    px: "padding-inline",
    py: "padding-block",
    m: "margin",
    mt: "margin-top",
    mr: "margin-right",
    mb: "margin-bottom",
    ml: "margin-left",
    mx: "margin-inline",
    my: "margin-block",
    gap: "gap",
    "gap-x": "column-gap",
    "gap-y": "row-gap",
    w: "width",
    h: "height",
    "min-w": "min-width",
    "max-w": "max-width",
    "min-h": "min-height",
    "max-h": "max-height",
    top: "top",
    right: "right",
    bottom: "bottom",
    left: "left",
    inset: "inset",
    "inset-x": "inset-inline",
    "inset-y": "inset-block",
    translate: "translate",
};

function utilityLine(name, property, value) {
    return `@utility ${name} { ${property}: ${value}; }`;
}

const lines = [
    "/* !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!",
    " * DO NOT EDIT THIS FILE MANUALLY.",
    " * It is generated from spacing-tokens.config.js by the generate:spacing-utilities",
    " * script. Run `npm run generate:spacing-utilities` to regenerate.",
    " * !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! */",
    "",
    "/* Remove all default spacing from the theme to ensure only EDS-derived tokens are used. */",
    "@theme {",
    "    --spacing: initial;",
    "}",
    "",
];

// ── Dynamic CSS custom properties ────────────────────────────────────────────

const dynamicTokens = Object.entries(tokens).filter(([, t]) => t.dynamic);
if (dynamicTokens.length > 0) {
    lines.push("/* Dynamic tokens exposed as CSS custom properties for runtime overrides */");
    lines.push(":root {");
    for (const [name, token] of dynamicTokens) {
        lines.push(`    --spacing-${name}: ${token.value};`);
    }
    lines.push("}");
    lines.push("");
}

// ── Density overrides ─────────────────────────────────────────────────────────

if (Object.keys(densityOverrides).length > 0) {
    lines.push(':where([data-density="comfortable"]) {');
    for (const [name, value] of Object.entries(densityOverrides)) {
        lines.push(`    --spacing-${name}: ${value};`);
    }
    lines.push("}");
    lines.push("");
}

// ── Spacing utilities ─────────────────────────────────────────────────────────

lines.push("/* ====== Spacing utilities =========================================== */");

let currentGroup = null;
for (const [name, token] of Object.entries(tokens)) {
    const group = name.split("-").slice(0, 2).join("-");
    if (group !== currentGroup) {
        currentGroup = group;
        lines.push(
            `\n/* ${name
                .split("-")
                .slice(0, 2)
                .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
                .join(" ")} */`,
        );
    }

    const value = token.dynamic ? `var(--spacing-${name})` : token.value;

    for (const groupName of token.groups) {
        const group = propertyGroups[groupName];

        if (!group) {
            throw new Error(`Unknown group "${groupName}" for token "${name}"`);
        }

        for (const [prefix, cssProperty] of Object.entries(group)) {
            lines.push(utilityLine(`${prefix}-${name}`, cssProperty, value));

            // Negative support
            if (
                token.negative &&
                [
                    "m",
                    "mt",
                    "mr",
                    "mb",
                    "ml",
                    "mx",
                    "my",
                    "top",
                    "right",
                    "bottom",
                    "left",
                    "inset",
                    "inset-x",
                    "inset-y",
                ].includes(prefix) &&
                value !== "0px"
            ) {
                lines.push(utilityLine(`-${prefix}-${name}`, cssProperty, `calc(${value} * -1)`));
            }
        }
    }
}

// ── px utilities ──────────────────────────────────────────────────────────────

lines.push("\n/* px — 1px fixed spacing */");
for (const [prefix, cssProperty] of Object.entries(PX_PROPERTIES)) {
    lines.push(utilityLine(`${prefix}-px`, cssProperty, "1px"));
}

lines.push(""); // trailing newline
writeFileSync(OUTPUT, lines.join("\n"));
console.log(`Generated ${OUTPUT}`);
