#!/usr/bin/env node
/**
 * Generates src/styles/generated/colors.css and Colors.stories.tsx from color-tokens.config.js.
 *
 * Usage:
 *   npm run generate:color-utilities
 */

import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import { fills, strokes, borders, text } from "../src/styles/theme/generator-configs/color-tokens.config.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT = resolve(__dirname, "../src/styles/generated/colors.css");

const BORDER_SIDES = [
    ["", "border-color"],
    ["-t", "border-top-color"],
    ["-r", "border-right-color"],
    ["-b", "border-bottom-color"],
    ["-l", "border-left-color"],
];

const OUTLINE_SIDES = [["", "outline-color"]];

function utilityLine(name, property, variable) {
    return `@utility ${name} { ${property}: var(${variable}); }`;
}

function sectionHeader(title) {
    const bar = "=".repeat(Math.max(0, 53 - title.length));
    return `\n/* ${"=".repeat(6)} ${title} ${bar} */\n`;
}

const lines = [
    "/* !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!",
    " * DO NOT EDIT THIS FILE MANUALLY.",
    " * It is generated from color-tokens.config.js by the generate:color-utilities",
    " * script. Run `npm run generate:color-utilities` to regenerate.",
    " * !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! */",
    "",
    "/* Remove all default colors from the theme, to ensure that all colors are derived from EDS tokens. */",
    "@theme {",
    "    --color-*: initial;",
    "}",
];

// ── Fills ────────────────────────────────────────────────────────────────────

lines.push(sectionHeader("Background/fill utilities"));
for (const [name, variable] of Object.entries(fills)) {
    lines.push(utilityLine(`bg-${name}`, "background-color", variable));
}

// ── Borders ──────────────────────────────────────────────────────────────────

lines.push(sectionHeader("Border utilities"));

// Group entries by their first segment (neutral, accent, …) for comments
let currentGroup = null;
for (const [name, variable] of Object.entries(borders)) {
    const group = name.split("-")[0];
    if (group !== currentGroup) {
        currentGroup = group;
        lines.push(`\n/* ${group.charAt(0).toUpperCase() + group.slice(1)} */`);
    }
    for (const [suffix, property] of BORDER_SIDES) {
        lines.push(utilityLine(`border${suffix}-${name}`, property, variable));
    }
}

// ── Outlines ─────────────────────────────────────────────────────────────────

lines.push(sectionHeader("Outline utilities"));

currentGroup = null;
for (const [name, variable] of Object.entries(borders)) {
    const group = name.split("-")[0];
    if (group !== currentGroup) {
        currentGroup = group;
        lines.push(`\n/* ${group.charAt(0).toUpperCase() + group.slice(1)} */`);
    }
    for (const [suffix, property] of OUTLINE_SIDES) {
        lines.push(utilityLine(`outline${suffix}-${name}`, property, variable));
    }
}

// ── Text ─────────────────────────────────────────────────────────────────────

lines.push(sectionHeader("Text color utilities"));

currentGroup = null;
for (const [name, variable] of Object.entries(text)) {
    const group = name.split("-")[0];
    if (group !== currentGroup) {
        currentGroup = group;
        lines.push(`\n/* ${group.charAt(0).toUpperCase() + group.slice(1)} */`);
    }
    lines.push(utilityLine(`text-${name}`, "color", variable));
}

// ── SVG fill (bg tokens) ──────────────────────────────────────────────────────

lines.push(sectionHeader("SVG fill utilities (bg tokens)"));
for (const [name, variable] of Object.entries(fills)) {
    lines.push(utilityLine(`fill-${name}`, "fill", variable));
}

// ── SVG stroke ───────────────────────────────────────────────────────────────

lines.push(sectionHeader("SVG stroke utilities"));
for (const [name, variable] of Object.entries(strokes)) {
    lines.push(utilityLine(`stroke-${name}`, "stroke", variable));
}

// ── SVG fill (text tokens) ────────────────────────────────────────────────────

lines.push(sectionHeader("SVG fill utilities (text tokens)"));

currentGroup = null;
for (const [name, variable] of Object.entries(text)) {
    const group = name.split("-")[0];
    if (group !== currentGroup) {
        currentGroup = group;
        lines.push(`\n/* ${group.charAt(0).toUpperCase() + group.slice(1)} */`);
    }
    lines.push(utilityLine(`fill-text-${name}`, "fill", variable));
}

lines.push(""); // trailing newline
writeFileSync(OUTPUT, lines.join("\n"));
console.log(`Generated ${OUTPUT}`);

// ── Stories ──────────────────────────────────────────────────────────────────

const STORIES_OUTPUT = resolve(__dirname, "../src/styles/generated/Colors.stories.tsx");

const CATEGORY_ORDER = ["neutral", "accent", "success", "info", "warning", "danger", "special"];

function getCategory(name) {
    for (const cat of ["neutral", "accent", "success", "info", "warning", "danger"]) {
        if (name === cat || name.startsWith(`${cat}-`)) return cat;
    }
    return "special";
}

function groupEntries(entries) {
    const groups = {};
    for (const [name, variable] of entries) {
        const cat = getCategory(name);
        if (!groups[cat]) groups[cat] = [];
        groups[cat].push({ name, variable });
    }
    return CATEGORY_ORDER.filter((cat) => groups[cat]).map((cat) => ({ group: cat, entries: groups[cat] }));
}

function serializeGroups(groups, prefix) {
    const lines = [];
    lines.push(`[`);
    for (const { group, entries } of groups) {
        lines.push(`    {`);
        lines.push(`        group: ${JSON.stringify(group.charAt(0).toUpperCase() + group.slice(1))},`);
        lines.push(`        entries: [`);
        for (const { name, variable } of entries) {
            lines.push(`            { name: ${JSON.stringify(`${prefix}${name}`)}, variable: ${JSON.stringify(variable)} },`);
        }
        lines.push(`        ],`);
        lines.push(`    },`);
    }
    lines.push(`]`);
    return lines;
}

const fillGroups = serializeGroups(groupEntries(Object.entries(fills)), "bg-");
const strokeGroups = serializeGroups(groupEntries(Object.entries(strokes)), "stroke-");
const borderGroups = serializeGroups(groupEntries(Object.entries(borders)), "border-");
const outlineGroups = serializeGroups(groupEntries(Object.entries(borders)), "outline-");
const textGroups = serializeGroups(groupEntries(Object.entries(text)), "text-");

const storyLines = [
    `/* DO NOT EDIT THIS FILE MANUALLY.`,
    ` * It is generated from color-tokens.config.js by the generate:color-utilities`,
    ` * script. Run \`npm run generate:color-utilities\` to regenerate. */`,
    ``,
    `import React from "react";`,
    `import type { Meta } from "@storybook/react";`,
    ``,
    `type ColorEntry = { name: string; variable: string };`,
    `type ColorGroup = { group: string; entries: ColorEntry[] };`,
    ``,
    `const FILLS: ColorGroup[] = `,
    ...fillGroups,
    `;`,
    ``,
    `const STROKES: ColorGroup[] = `,
    ...strokeGroups,
    `;`,
    ``,
    `const BORDERS: ColorGroup[] = `,
    ...borderGroups,
    `;`,
    ``,
    `const OUTLINES: ColorGroup[] = `,
    ...outlineGroups,
    `;`,
    ``,
    `const TEXT_COLORS: ColorGroup[] = `,
    ...textGroups,
    `;`,
    ``,
    `function Swatch({ name, variable, type }: { name: string; variable: string; type: "fill" | "stroke" | "border" | "outline" | "text" }) {`,
    `    const cssVar = \`var(\${variable})\`;`,
    `    const swatchStyle = { width: "3rem", height: "3rem", flexShrink: 0 } as const;`,
    `    let preview: React.ReactNode;`,
    `    if (type === "fill") {`,
    `        preview = <div className="rounded border border-neutral" style={{ ...swatchStyle, backgroundColor: cssVar }} />;`,
    `    } else if (type === "stroke") {`,
    `        preview = <svg style={swatchStyle} viewBox="0 0 48 48"><circle cx="24" cy="24" r="18" fill="none" stroke={cssVar} strokeWidth="4" /></svg>;`,
    `    } else if (type === "border") {`,
    `        preview = <div className="rounded" style={{ ...swatchStyle, border: \`3px solid \${cssVar}\` }} />;`,
    `    } else if (type === "outline") {`,
    `        preview = <div className="rounded" style={{ ...swatchStyle, outline: \`3px solid \${cssVar}\` }} />;`,
    `    } else {`,
    `        preview = <div className="flex items-center justify-center text-xl font-bold" style={{ ...swatchStyle, color: cssVar }}>Aa</div>;`,
    `    }`,
    `    return (`,
    `        <div className="flex items-center rounded hover:bg-neutral" style={{ gap: "1rem", padding: "0.375rem 0.5rem" }}>`,
    `            {preview}`,
    `            <div className="flex flex-col" style={{ gap: "0.25rem" }}>`,
    `                <div className="font-mono text-sm text-neutral-strong">{name}</div>`,
    `                <div className="font-mono text-neutral-subtle" style={{ fontSize: "0.65rem" }}>{variable}</div>`,
    `            </div>`,
    `        </div>`,
    `    );`,
    `}`,
    ``,
    `function Section({ title, groups, type }: { title: string; groups: ColorGroup[]; type: "fill" | "stroke" | "border" | "outline" | "text" }) {`,
    `    return (`,
    `        <section style={{ display: "flex", flexDirection: "column", gap: "1.5rem" }}>`,
    `            <h2 className="text-sm font-semibold text-neutral-strong uppercase tracking-wide pb-2 border-b border-neutral">`,
    `                {title}`,
    `            </h2>`,
    `            {groups.map(({ group, entries }) => (`,
    `                <div key={group} style={{ display: "flex", flexDirection: "column", gap: "0.25rem" }}>`,
    `                    <h3 className="text-xs font-semibold text-neutral-subtle uppercase tracking-wide" style={{ marginBottom: "0.25rem" }}>{group}</h3>`,
    `                    <div className="grid grid-cols-[repeat(auto-fill,minmax(280px,1fr))]">`,
    `                        {entries.map(({ name, variable }) => (`,
    `                            <Swatch key={name} name={name} variable={variable} type={type} />`,
    `                        ))}`,
    `                    </div>`,
    `                </div>`,
    `            ))}`,
    `        </section>`,
    `    );`,
    `}`,
    ``,
    `function ColorPalette() {`,
    `    return (`,
    `        <div className="p-6 bg-canvas" style={{ height: "100vh", overflowY: "auto", display: "flex", flexDirection: "column", gap: "3rem" }}>`,
    `            <Section title="Background / Fill" groups={FILLS} type="fill" />`,
    `            <Section title="Stroke" groups={STROKES} type="stroke" />`,
    `            <Section title="Border" groups={BORDERS} type="border" />`,
    `            <Section title="Outline" groups={OUTLINES} type="outline" />`,
    `            <Section title="Text" groups={TEXT_COLORS} type="text" />`,
    `            <div style={{ height: "30px", flexShrink: 0 }} />`,
    `        </div>`,
    `    );`,
    `}`,
    ``,
    `const meta: Meta = {`,
    `    title: "Design Tokens/Colors",`,
    `    parameters: {`,
    `        layout: "fullscreen",`,
    `    },`,
    `};`,
    ``,
    `export default meta;`,
    ``,
    `export const AllColors = {`,
    `    render: () => <ColorPalette />,`,
    `};`,
    ``,
];

writeFileSync(STORIES_OUTPUT, storyLines.join("\n"));
console.log(`Generated ${STORIES_OUTPUT}`);
