#!/usr/bin/env node
/**
 * Generates src/lib/mui-icons/index.tsx — one React component per MUI icon,
 * adding a numeric `size` prop (matching EDS Icon's API) alongside the standard
 * MUI props. When `size` is provided it sets `style.fontSize` to exact pixels.
 *
 * Usage:
 *   npm run generate:mui-icons
 */

import { writeFileSync, readFileSync } from "fs";
import { mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));

const TYPES_FILE = resolve(__dirname, "../node_modules/@mui/icons-material/index.d.ts");
const OUT_FILE = resolve(__dirname, "../src/lib/mui-icons/index.tsx");

const typesSource = readFileSync(TYPES_FILE, "utf-8");
const iconNames = [...typesSource.matchAll(/^export const (\w+): SvgIconComponent;/gm)].map((m) => m[1]);

if (iconNames.length === 0) {
    console.error("No icons found — check the types file path.");
    process.exit(1);
}

const components = iconNames
    .map((name) => {
        return (
            `export const ${name} = React.forwardRef<SVGSVGElement, MuiIconProps>(({ size, style, ...props }, ref) => (\n` +
            `    <Mui.${name} ref={ref} style={size !== undefined ? { fontSize: size, ...style } : style} {...props} />\n` +
            `));\n` +
            `${name}.displayName = "${name}";`
        );
    })
    .join("\n\n");

const output = `\
// This file is auto-generated by scripts/generate-mui-icons.js
// Do not edit manually. Run \`npm run generate:mui-icons\` to update.
import React from "react";
import type { SvgIconProps } from "@mui/material";
import * as Mui from "@mui/icons-material";

export type MuiIconProps = SvgIconProps & {
    /** Numeric pixel size, matching EDS Icon's \`size\` prop. Overrides \`fontSize\` when set. */
    size?: number;
};

${components}
`;

mkdirSync(resolve(__dirname, "../src/lib/mui-icons"), { recursive: true });
writeFileSync(OUT_FILE, output, "utf-8");
console.log(`Generated ${iconNames.length} icon components → ${OUT_FILE}`);
