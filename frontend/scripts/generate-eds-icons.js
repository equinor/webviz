#!/usr/bin/env node
/**
 * Generates src/lib/eds-icons/index.tsx — one React component per EDS icon,
 * so icons can be used directly (like MUI) without passing a `data` prop.
 *
 * Usage:
 *   npm run generate:eds-icons
 */

import { writeFileSync, readFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));

const TYPES_FILE = resolve(__dirname, "../node_modules/@equinor/eds-icons/dist/types/data.d.ts");
const OUT_FILE = resolve(__dirname, "../src/lib/eds-icons/index.tsx");

function toPascalCase(snake) {
    return snake
        .split("_")
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join("");
}

const typesSource = readFileSync(TYPES_FILE, "utf-8");
const iconNames = [...typesSource.matchAll(/^export declare const (\w+): IconData;/gm)].map((m) => m[1]);

if (iconNames.length === 0) {
    console.error("No icons found — check the types file path.");
    process.exit(1);
}

const components = iconNames
    .map((name) => {
        const pascal = toPascalCase(name);
        return (
            `export const ${pascal} = React.forwardRef<SVGSVGElement, EdsIconProps>((props, ref) => (\n` +
            `    <Icon data={icons.${name}} ref={ref} {...props} />\n` +
            `));\n` +
            `${pascal}.displayName = "${pascal}";`
        );
    })
    .join("\n\n");

const output = `\
// This file is auto-generated by scripts/generate-eds-icons.js
// Do not edit manually. Run \`npm run generate:eds-icons\` to update.
import React from "react";
import { Icon } from "@equinor/eds-core-react";
import type { IconProps } from "@equinor/eds-core-react";
import * as icons from "@equinor/eds-icons";

export type EdsIconProps = Omit<IconProps, "data" | "name">;

${components}
`;

writeFileSync(OUT_FILE, output, "utf-8");
console.log(`Generated ${iconNames.length} icon components → ${OUT_FILE}`);
